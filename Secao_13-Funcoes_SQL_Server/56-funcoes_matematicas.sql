
-- Funções de Manipulação de Número

-- Arredondar para um número inteiro maior
PRINT CEILING(4.4)

-- Arredondar para um número inteiro menor
PRINT FLOOR(4.9)

-- Gerar número aleatório entre 0 e 1
PRINT RAND()

-- Arredondar as casas decimais
PRINT ROUND(123.12312312, 4)

PRINT CONVERT(DECIMAL(15, 3), 123.12312312)

-- PI
PRINT PI()

-- Elevação da potência
PRINT POWER(4, 2)